package com.example.everhope;

public class UnbannedObj {
    String id;
    String name;
    String email;

    UnbannedObj(){
        id = "";
        name="";
        email="";
    }

    public String getName() {
        return name;
    }

    public String getId() {
        return id;
    }

    public String getEmail() {
        return email;
    }
}
